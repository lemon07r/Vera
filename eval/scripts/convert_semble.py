#!/usr/bin/env python3
"""Convert semble benchmark annotations to Vera eval task format.

Downloads annotation files and repos.json from semble's GitHub repo,
converts them to Vera's BenchmarkTask JSON format, and generates a
corpus manifest (semble-corpus.toml).

Usage:
    python3 eval/scripts/convert_semble.py [--output-dir eval/tasks/semble]
"""

import argparse
import json
import os
import sys
import urllib.request
from pathlib import Path

SEMBLE_RAW = "https://raw.githubusercontent.com/MinishLab/semble/main/benchmarks"
REPOS_URL = f"{SEMBLE_RAW}/repos.json"
ANNOTATIONS_URL = f"{SEMBLE_RAW}/annotations"

CATEGORY_MAP = {
    "symbol": "symbol_lookup",
    "semantic": "intent",
    "architecture": "cross_file",
}


def fetch_json(url: str):
    with urllib.request.urlopen(url) as resp:
        return json.loads(resp.read())


def convert_annotation(repo_name: str, entry: dict, idx: int) -> dict:
    category = CATEGORY_MAP.get(entry["category"], "intent")
    task_id = f"semble-{repo_name}-{category[:3]}-{idx:03d}"

    ground_truth = []
    for item in entry.get("relevant", []):
        if isinstance(item, str):
            ground_truth.append({
                "file_path": item,
                "line_start": 1,
                "line_end": 999999,
                "relevance": 2,
            })
        elif isinstance(item, dict):
            ground_truth.append({
                "file_path": item["path"],
                "line_start": item.get("start_line", 1),
                "line_end": item.get("end_line", 999999),
                "relevance": 2,
            })

    for item in entry.get("secondary", []):
        if isinstance(item, str):
            ground_truth.append({
                "file_path": item,
                "line_start": 1,
                "line_end": 999999,
                "relevance": 1,
            })
        elif isinstance(item, dict):
            ground_truth.append({
                "file_path": item["path"],
                "line_start": item.get("start_line", 1),
                "line_end": item.get("end_line", 999999),
                "relevance": 1,
            })

    return {
        "id": task_id,
        "query": entry["query"],
        "category": category,
        "repo": repo_name,
        "ground_truth": ground_truth,
        "description": f"[semble:{entry['category']}] {entry['query']}",
    }


def generate_corpus_toml(repos: list[dict], output_path: Path):
    lines = [
        '# Semble benchmark corpus (auto-generated by convert_semble.py)',
        '#',
        '# 63 repos across 19 languages, pinned to semble benchmark revisions.',
        '',
        '[corpus]',
        'version = 1',
        'description = "Semble benchmark corpus for cross-tool evaluation"',
        'clone_root = ".bench/semble-repos"',
        '',
    ]
    for repo in repos:
        lines.append('[[repos]]')
        lines.append(f'name = "{repo["name"]}"')
        lines.append(f'url = "{repo["url"]}"')
        lines.append(f'commit = "{repo["revision"]}"')
        lines.append(f'language = "{repo["language"]}"')
        lines.append(f'description = "semble benchmark repo"')
        if repo.get("benchmark_root"):
            lines.append(f'benchmark_root = "{repo["benchmark_root"]}"')
        lines.append('')

    output_path.write_text('\n'.join(lines))


def main():
    parser = argparse.ArgumentParser(description="Convert semble benchmarks to Vera format")
    parser.add_argument(
        "--output-dir",
        default="eval/tasks/semble",
        help="Output directory for Vera task JSON files",
    )
    parser.add_argument(
        "--corpus-output",
        default="eval/semble-corpus.toml",
        help="Output path for semble corpus manifest",
    )
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    print("Fetching semble repos.json...", file=sys.stderr)
    repos = fetch_json(REPOS_URL)
    print(f"  Found {len(repos)} repos", file=sys.stderr)

    total_tasks = 0
    per_category = {"symbol_lookup": 0, "intent": 0, "cross_file": 0}

    for repo in repos:
        name = repo["name"]
        url = f"{ANNOTATIONS_URL}/{name}.json"
        print(f"  Converting {name}...", file=sys.stderr, end=" ")

        try:
            annotations = fetch_json(url)
        except Exception as e:
            print(f"SKIP ({e})", file=sys.stderr)
            continue

        tasks = []
        for idx, entry in enumerate(annotations, 1):
            task = convert_annotation(name, entry, idx)
            tasks.append(task)
            cat = task["category"]
            per_category[cat] = per_category.get(cat, 0) + 1

        out_file = output_dir / f"{name}.json"
        out_file.write_text(json.dumps(tasks, indent=2) + "\n")
        total_tasks += len(tasks)
        print(f"{len(tasks)} tasks", file=sys.stderr)

    print(f"\nTotal: {total_tasks} tasks across {len(repos)} repos", file=sys.stderr)
    for cat, count in sorted(per_category.items()):
        print(f"  {cat}: {count}", file=sys.stderr)

    corpus_path = Path(args.corpus_output)
    generate_corpus_toml(repos, corpus_path)
    print(f"\nCorpus manifest: {corpus_path}", file=sys.stderr)
    print(f"Task files: {output_dir}/", file=sys.stderr)


if __name__ == "__main__":
    main()
